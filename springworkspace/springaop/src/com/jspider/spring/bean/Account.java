package com.jspider.spring.bean;

public class Account 
{
	private int accno;
	private int amt;
	public Account()
	{
		
	}
	public int getAccno() {
		return accno;
	}
	public void setAccno(int accno) {
		this.accno = accno;
	}
	public int getAmt() {
		return amt;
	}
	public void setAmt(int amt) {
		this.amt = amt;
	}
	

}
