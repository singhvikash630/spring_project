package com.jspider.spring.bean;

public class Employee 
{
	private int eno;
	private int sal;
	public Employee()
	{
		
	}
	public int getEno() {
		return eno;
	}
	public void setEno(int eno) {
		this.eno = eno;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}

}
